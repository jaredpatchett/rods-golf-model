def load_course_fit(target_course_name):
    """
    Returns (F, target_row) where F is the page-ready course-fit table and
    target_row is a {'scoreSD':, 'blowup':} dict for whichever row matches
    target_course_name (the REAL current event's course, from DataGolf's own
    event payload — see build()) — used to seed the sim's course-level variance.

    Falls back to TOUR-WIDE baseline defaults (not to whatever row happens to
    be first in the table) when no course-specific data exists for the current
    event. This matters: the hardcoded reference rows below are Royal Birkdale
    (an extreme, high-variance links course) and Bay Hill — silently defaulting
    a brand-new tournament at a different course to Birkdale's atypical numbers
    would distort every model probability, not just look slightly off.
    """
    def neutral_fallback():
        return {"scoreSD": TOUR_BASELINE_SD, "blowup": DEFAULT_BLOWUP}

    if os.path.exists(COURSE_FIT_TABLE):
        with open(COURSE_FIT_TABLE) as f:
            data = json.load(f)
        F = data.get("F") or []
        if F:
            target = next((row for row in F if target_course_name.lower() in str(row[0]).lower()), None)
            if target:
                return F, {"scoreSD": target[3], "blowup": target[4]}
            return F, neutral_fallback()
    F = course_fit_placeholder()
    target = next((row for row in F if target_course_name.lower() in str(row[0]).lower()), None)
    if target:
        return F, {"scoreSD": target[3], "blowup": target[4]}
    return F, neutral_fallback()


def fetch_matchups_with_fallback(key, tour):
    """
    Try tournament_matchups first (72-hole market); if DataGolf reports it's
    not being offered yet (confirmed on a live pull: match_list comes back as
    a string like "No tournament_matchups being offered right now." instead
    of a list), fall back to round_matchups, which was live and populated.
    Returns (rows, market_used, rounds_for_sim).
    """
    try:
        payload = fetch(EP["matchups"], key, tour=tour, market="tournament_matchups", odds_format="american")
        rows = as_rows(payload)
        if rows:
            return rows, "tournament_matchups", 4
        print("   tournament_matchups not posted yet — falling back to round_matchups")
    except SystemExit as e:
        print(f"   tournament_matchups failed ({e}) — falling back to round_matchups")

    try:
        payload = fetch(EP["matchups"], key, tour=tour, market="round_matchups", odds_format="american")
        rows = as_rows(payload)
        return rows, "round_matchups", 1
    except SystemExit as e:
        print(f"   round_matchups also failed ({e}) — leaving matchups empty")
        return [], None, 1


def build(tour, matchup_rounds=None, finish_trials=10000):
    key = get_key()
    print(f"[1/6] player decompositions ({tour}) ...")
    decomp = fetch(EP["decomp"], key, tour=tour)
    print(f"[2/6] skill ratings ...")
    skill = fetch(EP["skill"], key)  # not tour-specific — season-level skill ratings
    print(f"[3/6] pre-tournament predictions ({tour}) ...")
    pretourn = fetch(EP["pretourn"], key, tour=tour, odds_format="percent")  # win/top-N/make-cut probs;
    # not used directly — no projected-score field exists here (confirmed live). Kept in
    # case you want to sanity-check the finish sim against DataGolf's own probabilities.
    print(f"[4/6] field / tee times ({tour}) ...")
    field = fetch(EP["field"], key, tour=tour)
    print(f"[5/6] matchups ({tour}) ...")
    matchup_rows, market_used, auto_rounds = fetch_matchups_with_fallback(key, tour)
    print(f"   using market: {market_used} ({len(matchup_rows)} matchups)")
    print(f"[6/6] outrights (win/top5/top10/top20, {tour}) ...")
    # market param values per DataGolf's docs: win, top_5, top_10, top_20 (also make_cut/frl exist,
    # not pulled here — not asked for, and make_cut needs real cut-line modeling this sim doesn't do yet)
    FINISH_MARKETS = [("win", "win"), ("top_5", "top5"), ("top_10", "top10"), ("top_20", "top20")]
    outrights_markets = {}
    for dg_market, sim_key in FINISH_MARKETS:
        try:
            payload = fetch(EP["outrights"], key, tour=tour, market=dg_market, odds_format="american")
            outrights_markets[sim_key] = build_outrights_market(payload)
            print(f"   {dg_market}: {len(outrights_markets[sim_key])} players with a market price")
        except SystemExit as e:
            print(f"   {dg_market} unavailable ({e}) — that market will be model-only")
            outrights_markets[sim_key] = {}

    # explicit --matchup-rounds always wins if the caller passed one; otherwise
    # use whatever round count matches the market that actually had data
    n_rounds = matchup_rounds if matchup_rounds is not None else auto_rounds

    skill_by_id, skill_by_name = build_skill_ratings(skill)
    proj = build_projections(decomp, skill_by_id, skill_by_name)
    waves = build_waves(field)

    # Confirmed live (decompositions payload top-level keys): DataGolf's own response already
    # carries the real event_name and course_name — no reason to hardcode a specific
    # tournament's name here, which would silently mislabel every future event (and, worse,
    # mistag Tracker bets under the wrong tournament and feed the wrong course's variance
    # into the sim). Falls back to generic labels only if DataGolf's payload is missing them.
    event_name = decomp.get("event_name") or "Unknown Event"
    course_name = decomp.get("course_name") or event_name

    F, target_course_row = load_course_fit(course_name)
    proj_by_name = {r["name"]: {"mean": r["proj"], "sd": None} for r in proj}
    # NOTE: n_rounds sums that many simulated rounds per player in the sim, matched
    # automatically to whichever market actually had data (4 for tournament_matchups,
    # 1 for round_matchups). This ignores cuts/WDs on the 4-round path — a real 72-hole
    # matchup settles on made-cut rounds only, which this sim doesn't model yet.
    match = build_matchups(matchup_rows, waves, proj_by_name, target_course_row, n_rounds=n_rounds)

    print(f"   running full-field finish sim ({finish_trials} trials) ...")
    finish_sim_results = run_finish_sim(proj_by_name, course_row=target_course_row,
                                         n_rounds=4, n_trials=finish_trials)
    W = build_outrights(finish_sim_results, outrights_markets["win"], "win")
    W5 = build_outrights(finish_sim_results, outrights_markets["top5"], "top5")
    W10 = build_outrights(finish_sim_results, outrights_markets["top10"], "top10")
    W20 = build_outrights(finish_sim_results, outrights_markets["top20"], "top20")

    # convert to the compact array shapes the page's JS already expects
    P = [[r["name"], r["proj"], r["ttg"], r["ott"], r["app"], r["put"], r["arg"], r["fit"]] for r in proj]
    M = [[m["a"], m["b"], m["wave"], m["model"], m["market"], m["price"], m["priceB"], m["noVig"],
          m["skillGap"], m["bookCount"]] for m in match]

    data = {
        "meta": {
            "event": event_name,
            "course": course_name,
            "players": len(P),
            "generated": time.strftime("%Y-%m-%d %H:%M:%S"),
            "source": f"DataGolf projections (Path A) + sim_engine.py for model% [{market_used}]",
            "draw_bias": None,   # fill when you add forecast/results logic
        },
        "P": P, "F": F, "M": M, "W": W, "W5": W5, "W10": W10, "W20": W20,
    }
    with open(OUT, "w") as f:
        json.dump(data, f, indent=1)
    print(f"\nWrote {OUT}: event='{event_name}' course='{course_name}' — {len(P)} players, {len(M)} matchups, "
          f"{len(W)}/{len(W5)}/{len(W10)}/{len(W20)} win/top5/top10/top20 rows.")
    return data


def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--tour", default="pga", help="pga | euro | opp | alt (try euro if The Open isn't on pga feed)")
    ap.add_argument("--watch", type=int, default=0, help="re-pull every N seconds (0 = once)")
    ap.add_argument("--matchup-rounds", type=int, default=None,
                     help="rounds summed per player in the sim. Default: auto-detected from "
                          "whichever market actually has data (4 for tournament_matchups, "
                          "1 for round_matchups). Set explicitly to override.")
    ap.add_argument("--finish-trials", type=int, default=10000,
                     help="Monte Carlo trials for the full-field win-probability sim. "
                          "10000 runs in a few seconds for a ~156-player field; raise for "
                          "tighter estimates, lower if the pipeline needs to run faster.")
    args = ap.parse_args()
    if args.watch:
        print(f"watch mode: every {args.watch}s (ctrl-C to stop)")
        while True:
            try:
                build(args.tour, matchup_rounds=args.matchup_rounds, finish_trials=args.finish_trials)
            except SystemExit as e:
                print(e)
            time.sleep(args.watch)
    else:
        build(args.tour, matchup_rounds=args.matchup_rounds, finish_trials=args.finish_trials)


if __name__ == "__main__":
    main()
