def get_key():
    key = os.environ.get("DG_KEY")
    if not key:
        sys.exit("ERROR: set your key first ->  export DG_KEY=\"your_datagolf_key\"")
    return key


def fetch(endpoint, key, **params):
    """GET a DataGolf feed as JSON. Returns parsed python or raises."""
    params.setdefault("file_format", "json")
    params["key"] = key
    url = f"{BASE}{endpoint}?{urllib.parse.urlencode(params)}"
    try:
        with urllib.request.urlopen(url, timeout=30) as r:
            raw = r.read().decode("utf-8")
    except urllib.error.HTTPError as e:
        # surface DataGolf's own error message (bad key, wrong tour, no event, etc.)
        body = e.read().decode("utf-8", "ignore")
        raise SystemExit(f"HTTP {e.code} on {endpoint}: {body[:300]}")
    except urllib.error.URLError as e:
        raise SystemExit(f"Network error on {endpoint}: {e.reason}")
    try:
        return json.loads(raw)
    except json.JSONDecodeError:
        raise SystemExit(f"Non-JSON reply on {endpoint} (first 200 chars): {raw[:200]}")


def as_rows(payload):
    """DataGolf wraps rows in different keys depending on endpoint; normalize to a list."""
    if isinstance(payload, list):
        return payload
    # 'odds' is here specifically for outrights: confirmed live that its top-level payload
    # has BOTH 'books_offering' (a flat list of book name strings) and 'odds' (the real
    # 156-player list) — the generic fallback below picked books_offering first since it's
    # also a list and came first in the dict, silently truncating "the field" to 13 book names.
    for k in ("players", "matchups", "match_list", "odds", "field", "data", "baseline", "rows"):
        if isinstance(payload, dict) and isinstance(payload.get(k), list):
            return payload[k]
    # some feeds nest under a single dict key -> take first list value found
    if isinstance(payload, dict):
        for v in payload.values():
            if isinstance(v, list):
                return v
    return []


def num(x, nd=1):
    """Round to nd decimals, pass through None for missing (renders as 'nan')."""
    if x is None or x == "":
        return None
    try:
        return round(float(x), nd)
    except (TypeError, ValueError):
        return None


# ------------------------------------------------------------------
# MAP 1: projections table (the `P` array)
# ------------------------------------------------------------------
# Approximate baseline round score used to convert final_pred (a strokes-
# gained-vs-field prediction, confirmed live to run ~2.0-2.7 for the top of
# the field) into an absolute score for the PROJ column. This is a rough
# constant, not derived from real course data — Path B (computing your own
# Birkdale scoring baseline from round-level history, see course_fit_engine.py)
# replaces this with something real. Until then it's directionally correct
# (better predicted players get a lower/better PROJ number) but the absolute
# number is an approximation. 71.0 ≈ Birkdale's par 70 + a small toughening
# margin typical of Open Championship setups.
BASELINE_ROUND_SCORE = 71.0


def build_skill_ratings(skill_payload):
    """
    Map DataGolf skill-ratings -> {dg_id: {ttg, ott, app, put, arg}, name: {...}}
    This is the actual source of category strokes-gained (confirmed via DataGolf's
    docs: sg_putt, sg_arg, sg_app, sg_ott, sg_t2g, sg_bs, sg_total, ...).
    Indexed by BOTH dg_id and normalized name since join reliability across
    DataGolf's different feeds is inconsistent — dg_id is preferred when present.
    """
    def pick(row, *names):
        for n in names:
            if n in row and row[n] is not None:
                return row[n]
        return None

    by_id, by_name = {}, {}
    for r in as_rows(skill_payload):
        vals = {
            "ttg": num(pick(r, "sg_t2g", "t2g")),
            "ott": num(pick(r, "sg_ott", "ott")),
            "app": num(pick(r, "sg_app", "app")),
            "put": num(pick(r, "sg_putt", "sg_put", "putt")),
            "arg": num(pick(r, "sg_arg", "arg")),
        }
        dg_id = pick(r, "dg_id")
        name = pick(r, "player_name", "name")
        if dg_id is not None:
            by_id[dg_id] = vals
        if name:
            by_name[normalize_name(name)] = vals
    return by_id, by_name


def build_projections(decomp_payload, skill_by_id, skill_by_name):
    """
    Map DataGolf player-decompositions + skill-ratings -> Rod's `P` rows:
      [name, proj, ttg, ott, app, put, arg, fit]

    - name/dg_id/proj/fit come from player-decompositions (confirmed live fields:
      player_name, dg_id, final_pred, total_fit_adjustment)
    - ttg/ott/app/put/arg come from skill-ratings, joined by dg_id (falls back
      to normalized name if dg_id isn't present in either feed)
    """
    def pick(row, *names):
        for n in names:
            if n in row and row[n] is not None:
                return row[n]
        return None

    rows = as_rows(decomp_payload)
    out = []
    for r in rows:
        name = pick(r, "player_name", "name")
        if not name:
            continue
        norm_name = normalize_name(name)
        dg_id = pick(r, "dg_id")
        skill = skill_by_id.get(dg_id) if dg_id is not None else None
        if skill is None:
            skill = skill_by_name.get(norm_name, {})

        final_pred = pick(r, "final_pred", "baseline_pred")
        proj = num(BASELINE_ROUND_SCORE - final_pred) if final_pred is not None else None

        out.append({
            "name": norm_name,
            "proj": proj,
            "ttg":  skill.get("ttg"),
            "ott":  skill.get("ott"),
            "app":  skill.get("app"),
            "put":  skill.get("put"),
            "arg":  skill.get("arg"),
            "fit":  num(pick(r, "total_fit_adjustment", "course_fit", "fit", "fit_adj", "cf")),
            "_dg_id": dg_id,
        })
    # sort best (lowest) proj first; None sinks to bottom
    out.sort(key=lambda x: (x["proj"] is None, x["proj"] if x["proj"] is not None else 999))
    return out


# ------------------------------------------------------------------
# MAP 2: matchups table (the `M` array)
# ------------------------------------------------------------------
# Book preference order when picking which sportsbook's price to show as
# "market" — real books first (most consistently present across matchups per
# the live sample), datagolf's own line last since that's their model output,
# not a market price, and we want market% to be genuinely market-sourced.
BOOK_PREFERENCE = ("draftkings", "bet365", "bovada", "betcris", "fanduel", "pinnacle", "datagolf")
# draftkings is first because that's the book Rod actually has an account with — the price
# shown on the page should be the price he can actually get, not just the first one found.
# Falls back down the list only when DraftKings hasn't posted a price for that player/market.


def pick_book_price(odds_dict):
    """
    DataGolf's real shape (confirmed on a live pull): odds is nested per book,
    e.g. {"bet365": {"p1": "-111", "p2": "-111"}, "bovada": {...}, ...} — not
    a flat p1_odds field. Walk BOOK_PREFERENCE and return BOTH players' prices
    from the first book that has them, plus which book it came from. Both
    sides are needed (not just p1) so a FADE call — which means betting the
    *other* player — can still show the correct real price rather than
    reusing p1's price for a bet that's actually on p2.

    Also returns book_count — how many books actually posted a price for this
    matchup at all, not just which one we ended up using. A price backed by
    one thin book deserves less confidence than one where 7 books agree, even
    if the number itself looks the same on the page.
    """
    if not isinstance(odds_dict, dict):
        return None, None, None, 0
    book_count = sum(1 for entry in odds_dict.values()
                      if isinstance(entry, dict) and entry.get("p1") is not None)
    for book in BOOK_PREFERENCE:
        entry = odds_dict.get(book)
        if isinstance(entry, dict) and entry.get("p1") is not None:
            return entry["p1"], entry.get("p2"), book, book_count
    # fall back to whatever book is present, in whatever order the dict gives us
    for book, entry in odds_dict.items():
        if isinstance(entry, dict) and entry.get("p1") is not None:
            return entry["p1"], entry.get("p2"), book, book_count
    return None, None, None, book_count


def pick_single_book_price(row):
    """
    Same book-preference walk as pick_book_price, but for outrights markets
    where each book's price sits directly on the row as a flat field — e.g.
    {"bet365": "+500", "draftkings": "+450", ..., "player_name": "...", "dg_id": ...}
    — confirmed live: NOT nested under a sub-'odds' key the way matchups is.
    "datagolf" is excluded even though it's in BOOK_PREFERENCE's fallback
    position — confirmed live it's a nested {"baseline":, "baseline_history_fit":}
    object, not a plain price, and would silently return a dict as a "price"
    if not filtered out here.
    """
    if not isinstance(row, dict):
        return None, None
    for book in BOOK_PREFERENCE:
        if book == "datagolf":
            continue  # not a real market price — see docstring
        entry = row.get(book)
        if isinstance(entry, (str, int, float)):
            return entry, book
    # fall back to any other book-shaped field present, skipping known non-price keys
    skip_keys = {"player_name", "name", "dg_id", "datagolf"}
    for k, entry in row.items():
        if k in skip_keys:
            continue
        if isinstance(entry, (str, int, float)):
            return entry, k
    return None, None


def build_matchups(matchup_payload, wave_by_name, proj_by_name, course_row, n_rounds=1):
    """
    Map DataGolf matchup tool -> Rod's `M` rows:
      [playerA, playerB, wave, modelPct, marketPct, priceA, priceB]

    Pulls the two players and the book price/market number from DataGolf, but
    the model% comes from sim_engine.py (projected scores + this event's
    course variance/blowup), NOT from DataGolf's own matchup probability —
    see the module docstring for why that swap matters.

    priceB (player B's own price) is captured alongside priceA so a FADE call
    — which means betting player B, not A — can show a real, correct price
    instead of reusing A's price for a bet that's actually on B.
    """
    def pick(row, *names):
        for n in names:
            if n in row and row[n] is not None:
                return row[n]
        return None

    raw_rows = as_rows(matchup_payload)

    # DataGolf posts the SAME pairing as two separate entries when a book offers both a
    # standard 2-way market (ties refunded — "ties": "void") and a 3-way market (ties as
    # their own outcome — "ties": "separate bet offered"). Confirmed live: the 3-way variant
    # carries far fewer books and never includes DraftKings, so keeping both just duplicates
    # every such pairing on the page with a worse, less-actionable price.
    #
    # SEPARATE, BIGGER ISSUE (confirmed against Rod's real DraftKings tee-group list): the feed
    # also contains "alternate 2-ball" pairings between players who are NOT in the same tee
    # group at all — e.g. "McIlroy vs Schauffele" when their real groups are McIlroy/Bridgeman
    # and Fox/Schauffele. These alternates only ever appear ONCE, as "ties":"void", with no
    # "ties":"separate bet offered" sibling for that exact pair. Every genuine tee-group pairing
    # in Rod's real list appeared TWICE — both tie structures — except one (missing its void
    # entry entirely on DataGolf's end). So: a pair is a real tee-group matchup if a "separate
    # bet offered" entry exists for it (use the void entry for the actual price if one exists,
    # since that's the bettable 2-way market; fall back to the separate-bet entry's price only
    # if no void version showed up). Pairs with ONLY a void entry and no separate-bet sibling
    # are alternates and get dropped from this table entirely.
    by_pair = defaultdict(list)
    for r in raw_rows:
        p1_raw = pick(r, "p1_player_name", "player1", "p1")
        p2_raw = pick(r, "p2_player_name", "player2", "p2")
        if not p1_raw or not p2_raw:
            continue
        by_pair[(p1_raw, p2_raw)].append(r)

    rows = []
    for key, entries in by_pair.items():
        tie_types = {e.get("ties") for e in entries}
        if "separate bet offered" not in tie_types:
            continue  # alternate/cross-group pairing, not a real tee-group matchup — drop it
        void_entry = next((e for e in entries if e.get("ties") == "void"), None)
        rows.append(void_entry if void_entry is not None else entries[0])

    pairs = []
    meta = []
    for r in rows:
        p1 = pick(r, "p1_player_name", "player1", "p1")
        p2 = pick(r, "p2_player_name", "player2", "p2")
        if not p1 or not p2:
            continue
        p1 = normalize_name(p1); p2 = normalize_name(p2)
        # book implied — DataGolf nests odds per sportsbook; walk BOOK_PREFERENCE
        market = pick(r, "p1_implied_prob", "market_prob", "p1_book_prob")
        priceA, priceB, book_used, book_count = pick_book_price(r.get("odds"))
        market_prob = as_prob(market)
        if market_prob is None and priceA is not None:
            market_prob = implied_prob_from_american(priceA)  # derive from the price if DG didn't give a % directly
        w1 = wave_by_name.get(p1, "?"); w2 = wave_by_name.get(p2, "?")
        wave = w1 if w1 == w2 else f"{w1}/{w2}"
        pairs.append((p1, p2))
        priceA_display = f"{priceA} ({book_used})" if priceA is not None else "—"
        priceB_display = f"{priceB} ({book_used})" if priceB is not None else "—"
        # real no-vig: normalize both sides' implied probabilities so they sum to exactly
        # 1.0, removing the book's overround. Previously this column was a placeholder that
        # just copied market% unchanged — confirmed on the live page (every row showed
        # No-Vig% == Market%, which should never happen once real vig is actually removed).
        no_vig = None
        if priceA is not None and priceB is not None:
            impliedA = implied_prob_from_american(priceA)
            impliedB = implied_prob_from_american(priceB)
            if impliedA is not None and impliedB is not None and (impliedA + impliedB) > 0:
                no_vig = impliedA / (impliedA + impliedB)
        # skill-gap flag: large PROJ differences are exactly where the flat, shared variance
        # assumption (same sd for every player) breaks down hardest, and real books tend to
        # price "obvious" mismatches efficiently — so a big edge on a big skill gap is more
        # often model error than real value, not less. Flag it rather than hide it; the person
        # placing the bet should see the reason to double-check, not have it auto-filtered.
        meanA = proj_by_name.get(p1, {}).get("mean")
        meanB = proj_by_name.get(p2, {}).get("mean")
        skill_gap = abs(meanA - meanB) if (meanA is not None and meanB is not None) else None
        meta.append({"a": p1, "b": p2, "wave": wave, "market": market_prob,
                      "price": priceA_display, "priceB": priceB_display, "noVig": no_vig,
                      "skillGap": round(skill_gap, 2) if skill_gap is not None else None,
                      "bookCount": book_count})

    # one batched sim call for every matchup on the slate
    model_probs = run_matchup_sim(proj_by_name, pairs, course_row=course_row, n_rounds=n_rounds)

    out = []
    for (p1, p2), m in zip(pairs, meta):
        m["model"] = model_probs.get((p1, p2))  # None if either player had no projection to sim from
        out.append(m)

    # sort by edge desc; unsimmed/unmarketed rows sink to the bottom
    def edge(m):
        if m["model"] is None or m["market"] is None:
            return -999
        return m["model"] - m["market"]
    out.sort(key=edge, reverse=True)
    return out


