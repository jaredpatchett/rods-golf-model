# MAP 4: outright win market (the `W` array) — tournament winner odds
# ------------------------------------------------------------------
def build_outrights_market(outrights_payload):
    """
    Map DataGolf's outrights market -> {normalized_name: {"market": prob, "price": display_str}}
    Single-outcome market (unlike matchups' p1/p2 pairs), so each book maps
    to one price per player — see pick_single_book_price.
    """
    def pick(row, *names):
        for n in names:
            if n in row and row[n] is not None:
                return row[n]
        return None

    out = {}
    for r in as_rows(outrights_payload):
        name = pick(r, "player_name", "name")
        if not name:
            continue
        norm_name = normalize_name(name)
        price, book_used = pick_single_book_price(r)  # prices sit directly on the row, confirmed live
        market_prob = implied_prob_from_american(price) if price is not None else None
        out[norm_name] = {
            "market": market_prob,
            "price": f"{price} ({book_used})" if price is not None else "—",
        }
    return out


def build_outrights(finish_sim_results, outrights_market, sim_key):
    """
    Merge the full-field finish simulation (sim_engine.run_finish_sim) with
    DataGolf's real market odds for one market -> rows:
      [name, modelPct, marketPct, price]
    sim_key selects which of the sim's outputs to use: 'win', 'top5', 'top10', 'top20'.
    Included for every player the sim has a result for, even if no market
    price was found (renders as "—" on the page, same convention as the
    other tables).
    """
    out = []
    for name, sim_row in finish_sim_results.items():
        mkt = outrights_market.get(name, {})
        out.append([name, round(sim_row[sim_key], 4), mkt.get("market"), mkt.get("price", "—")])
    out.sort(key=lambda r: -r[1])  # best model% first
    return out


def as_prob(x):
    """Coerce a probability that might be 0-1 or 0-100 into a 0-1 float."""
    if x is None or x == "":
        return None
    try:
        v = float(x)
    except (TypeError, ValueError):
        return None
    return v / 100.0 if v > 1.0 else v


def implied_prob_from_american(price):
    """American odds -> implied probability (still vig-inclusive; this is 'market', not 'no-vig')."""
    try:
        p = float(price)
    except (TypeError, ValueError):
        return None
    if p > 0:
        return 100.0 / (p + 100.0)
    elif p < 0:
        return -p / (-p + 100.0)
    return None


