# MAP 3: wave / draw from field updates
# ------------------------------------------------------------------
def build_waves(field_payload):
    """
    Map field-updates -> {player_name: 'AM'|'PM'} using round-1 tee time,
    and compute a draw-bias placeholder (real bias needs forecast + results).
    """
    def pick(row, *names):
        for n in names:
            if n in row and row[n] is not None:
                return row[n]
        return None
    waves = {}
    for r in as_rows(field_payload):
        nm = normalize_name(pick(r, "player_name", "name") or "")
        tt = pick(r, "r1_teetime", "teetime", "tee_time")
        if not nm or not tt:
            continue
        waves[nm] = classify_wave(tt)
    return waves


def classify_wave(teetime):
    """Very rough AM/PM split from a tee-time string like '2026-07-16T13:20' or '1:20 PM'."""
    s = str(teetime).lower()
    if "pm" in s:
        # 12-something pm is borderline; treat >=12:00 as PM
        return "PM"
    if "am" in s:
        return "AM"
    # ISO-ish: grab the hour
    for sep in ("t", " "):
        if sep in s:
            tail = s.split(sep)[-1]
            hh = tail[:2]
            if hh.isdigit():
                return "AM" if int(hh) < 12 else "PM"
    return "?"


# ------------------------------------------------------------------
# name normalization so the three feeds join cleanly
# ------------------------------------------------------------------
def normalize_name(name):
    """DataGolf often returns 'Last, First'; the page shows 'First Last'. Normalize to 'First Last'."""
    name = name.strip()
    if "," in name:
        last, first = [p.strip() for p in name.split(",", 1)]
        return f"{first} {last}"
    return name


